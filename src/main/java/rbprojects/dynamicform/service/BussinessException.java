package rbprojects.dynamicform.service;

public class BussinessException extends Exception {

	public BussinessException() {
		
	}

	public BussinessException(String arg0) {
		super(arg0);
		
	}

	public BussinessException(Throwable arg0) {
		super(arg0);
		
	}

	public BussinessException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public BussinessException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
		
	}

}
